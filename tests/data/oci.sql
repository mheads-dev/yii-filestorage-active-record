BEGIN EXECUTE IMMEDIATE 'DROP TRIGGER "product_BI"'; EXCEPTION WHEN OTHERS THEN IF SQLCODE != -4080 THEN RAISE; END IF; END;--
BEGIN EXECUTE IMMEDIATE 'DROP TABLE "product"'; EXCEPTION WHEN OTHERS THEN IF SQLCODE != -942 THEN RAISE; END IF; END;--
BEGIN EXECUTE IMMEDIATE 'DROP SEQUENCE "PRODUCT_SEQ"'; EXCEPTION WHEN OTHERS THEN IF SQLCODE != -2289 THEN RAISE; END IF; END;--

/* STATEMENTS */

CREATE TABLE "product"
(
	"id"         INTEGER NOT NULL,
	"name"       VARCHAR2(255) NOT NULL,
	"picture_id" NUMBER(20) NULL,
	"manual_id"  NUMBER(20) NULL,
	CONSTRAINT "product_PK" PRIMARY KEY ("id") ENABLE
);

CREATE SEQUENCE "PRODUCT_SEQ";

CREATE INDEX "idx_product_picture_id" ON "product" ("picture_id");
CREATE INDEX "idx_product_manual_id" ON "product" ("manual_id");

/* TRIGGERS */

CREATE OR REPLACE TRIGGER "product_BI"
BEFORE INSERT ON "product"
FOR EACH ROW
BEGIN
	<<COLUMN_SEQUENCES>>
	BEGIN
		IF INSERTING AND :NEW."id" IS NULL THEN
			SELECT "PRODUCT_SEQ".NEXTVAL INTO :NEW."id" FROM SYS.DUAL;
		END IF;
	END COLUMN_SEQUENCES;
END;
